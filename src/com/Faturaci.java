    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        System.out.println("Colum: "+jTable1.getSelectedColumn()+" Row:"+jTable1.getSelectedRow());
        basketList.addElement(jTable1.getValueAt(jTable1.getSelectedRow(),2));
        basketListPrice.addElement(jTable1.getValueAt(jTable1.getSelectedRow(),3));
        
        Guncelle b;
        count=Integer.parseInt( jSpinner1.getValue().toString())-1;
        b=new Guncelle(connn, Integer.parseInt(IdText.getText()), Integer.parseInt(jTextField3.getText()), jTextField4.getText(), Float.parseFloat(jTextField5.getText()), Integer.parseInt( jComboBox1.getSelectedItem().toString()), count );
        
        asd.ListEkle(jTable1.getValueAt(jTable1.getSelectedRow(),2).toString(),Double.parseDouble(jTable1.getValueAt(jTable1.getSelectedRow(),3).toString()));

        jList1.setModel(basketList);
        jList2.setModel(basketListPrice);
        System.out.println(jTable1.getValueAt(jTable1.getSelectedRow(),3));
        
        double totalNum = 0;
        
        for(int i=0; i<basketListPrice.size(); i++){
            totalNum += Double.parseDouble(basketListPrice.get(i).toString());
        }
        jLabel5.setText(""+totalNum);
        Show_All();
    }//GEN-LAST:event_jButton3ActionPerformed

