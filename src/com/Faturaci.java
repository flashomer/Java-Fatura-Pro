    public void Show_All(){
        
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        
        BilgilieriGetir bilgilist=new BilgilieriGetir(connn);
        ArrayList<Bilgiler> list= bilgilist.getBilgi();
        
        
        Object[] row=new Object[6];
        model.setRowCount(0);
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getId();
            row[1]=list.get(i).getStockCode();
            row[2]=list.get(i).getDescription();
            row[3]=list.get(i).getUnitPrice();
            row[4]=list.get(i).getTaxRate();
            row[5]=list.get(i).getAmount();
            model.addRow(row);
        }    
    }
    
    public void Show_Customer(){
        DefaultTableModel model=(DefaultTableModel)jTable2.getModel();
        
        CustomerGetir custlist=new CustomerGetir(connn);
        ArrayList<CustomerInfos> list=custlist.getCustomer();
        
        Object[] row=new Object[5];
        model.setRowCount(0);
        
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getTaxNo();
            row[1]=list.get(i).getTaxOffice();
            row[2]=list.get(i).getCustName();
            row[3]=list.get(i).getCustAddress();
            row[4]=list.get(i).getCustTel();
            model.addRow(row);
        }
    }
    
